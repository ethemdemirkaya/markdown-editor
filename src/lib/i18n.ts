const DICTIONARIES: Record<Locale, Dict> = { en, tr, de, fr, es };

export function translate(key: string, loc: Locale, vars?: Record<string, string>): string {
  let value = DICTIONARIES[loc]?.[key] ?? DICTIONARIES.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      value = value.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    }
  }
  return value;
}

export const t = derived(locale, ($locale) => {
  return (key: string, vars?: Record<string, string>) => translate(key, $locale, vars);
});

export function tNow(key: string, vars?: Record<string, string>): string {
  return translate(key, get(locale), vars);
}
