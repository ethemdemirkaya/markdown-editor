export const SUPPORTED_LOCALES = ['en', 'tr', 'de', 'fr', 'es'] as const;
export type Locale = (typeof SUPPORTED_LOCALES)[number];

export const LOCALE_LABELS: Record<Locale, string> = {
  en: 'English',
  tr: 'Türkçe',
  de: 'Deutsch',
  fr: 'Français',
  es: 'Español',
};

const STORAGE_KEY = 'markdown-editor:locale';

function detectInitial(): Locale {
  if (!browser) return 'en';
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved && (SUPPORTED_LOCALES as readonly string[]).includes(saved)) return saved as Locale;
  const navLang = (navigator.language || 'en').slice(0, 2).toLowerCase();
  return (SUPPORTED_LOCALES as readonly string[]).includes(navLang) ? (navLang as Locale) : 'en';
}

export const locale = writable<Locale>(detectInitial());

if (browser) {
  locale.subscribe((value) => {
    try {
      localStorage.setItem(STORAGE_KEY, value);
      document.documentElement.lang = value;
    } catch {
      // ignore
    }
  });
}

